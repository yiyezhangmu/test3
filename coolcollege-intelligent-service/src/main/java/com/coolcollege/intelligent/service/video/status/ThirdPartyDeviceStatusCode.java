    /**
     * 根据状态码获取错误信息
     * @param statusCode 状态码
     * @return 错误信息，如果状态码不存在则返回"未知错误码"
     */
    public static String getJfyErrorMessage(int statusCode) {
        return JFY_STATUS_CODE_MAP.getOrDefault(statusCode, "调用杰峰云API错误");
    }

    /**
     * 检查状态码是否存在
     * @param statusCode 状态码
     * @return 是否存在
     */
    public static boolean containsCode(int statusCode) {
        return JFY_STATUS_CODE_MAP.containsKey(statusCode);
    }



